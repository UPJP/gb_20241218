package condition;

public class main01 {

	public static void main(String[] args) {
		int myage = 19;

		if (myage 
				== 19) {
			System.out.println("19살 입니다.");
		}
		if (myage 
				!= 20) {
			System.out.println("19살 아닙니다.");
		}
		if (myage > 19) {
			System.out.println("성인 입니다.");
		}
		if (myage 
				<= 19) {
			System.out.println("미자 입니다.");
		}
	}

}
